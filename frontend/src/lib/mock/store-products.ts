export function getMockStoreProducts(
  pageSize = 12,
): StrapiListResponse<StoreProductSummary> {
  const items = MOCK_PRODUCTS.slice(0, pageSize)
  return {
    data: items,
    meta: {
      pagination: {
        page: 1,
        pageSize,
        pageCount: 1,
        total: items.length,
      },
    },
  }
}

export function getMockStoreProduct(slug: string): StrapiSingleResponse<StoreProduct> | null {
  const product = MOCK_PRODUCTS.find((p) => p.slug === slug)
  if (!product) return null
  return { data: product, meta: {} }
}
